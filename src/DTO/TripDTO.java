package DTO;

public class TripDTO {
	int TripNum;
	String TripName;
	String TripPay;
	String TripDays;
	String TripStartDate;
	String TripFinishDate;
	String TripInfo;
	String TripSpot;

	public int getTripNum() {
		return TripNum;
	}

	public void setTripNum(int tripNum) {
		TripNum = tripNum;
	}

	public String getTripName() {
		return TripName;
	}

	public void setTripName(String tripName) {
		TripName = tripName;
	}

	public String getTripPay() {
		return TripPay;
	}

	public void setTripPay(String tripPay) {
		TripPay = tripPay;
	}

	public String getTripDays() {
		return TripDays;
	}

	public void setTripDays(String tripDays) {
		TripDays = tripDays;
	}

	public String getTripStartDate() {
		return TripStartDate;
	}

	public void setTripStartDate(String tripStartDate) {
		TripStartDate = tripStartDate;
	}

	public String getTripFinishDate() {
		return TripFinishDate;
	}

	public void setTripFinishDate(String tripFinishDate) {
		TripFinishDate = tripFinishDate;
	}

	public String getTripInfo() {
		return TripInfo;
	}

	public void setTripInfo(String tripInfo) {
		TripInfo = tripInfo;
	}

	public String getTripSpot() {
		return TripSpot;
	}

	public void setTripSpot(String tripSpot) {
		TripSpot = tripSpot;
	}

}
