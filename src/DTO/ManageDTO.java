package DTO;

public class ManageDTO {
	String id;
	String cname;
	int TripNum;
	String TripName;
	String tripRv;
	
	public String getTripRv() {
		return tripRv;
	}
	public void setTripRv(String tripRv) {
		this.tripRv = tripRv;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getTripNum() {
		return TripNum;
	}
	public void setTripNum(int tripNum) {
		TripNum = tripNum;
	}
	public String getTripName() {
		return TripName;
	}
	public void setTripName(String tripName) {
		TripName = tripName;
	}
	

	
}